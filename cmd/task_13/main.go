// Обмен значениями без третьей переменной
//
// Поменять местами два числа без использования временной переменной.
//
// Подсказка: примените сложение/вычитание или XOR-обмен.
////////////////////////////////////////////////////////////////////

package main

import "fmt"

func main() {
	fmt.Println("Hello, World!")

	a, b := 3, 5
	fmt.Printf("До обмена: a=%d, b=%d\n", a, b)

	// Способ 1: через сложение/вычитание
	// a = a + b // теперь a = 8
	// b = a - b // теперь b = 3
	// a = a - b // теперь a = 5

	// Способ 2: через XOR-обмен
	a = a ^ b
	b = a ^ b
	a = a ^ b

	fmt.Printf("После обмена: a=%d, b=%d\n", a, b)

}
